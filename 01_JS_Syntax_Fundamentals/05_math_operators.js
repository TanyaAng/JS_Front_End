function solve (x,y, operator){
    switch (operator){
        case '+' : console.log(x+y); break;
        case '-' : console.log(x-y); break;
        case '*' : console.log(x*y); break;
        case '/' : console.log(x/y); break;
        case '%' : console.log(x%y); break;
        case '**' : console.log(x**y); break;
    }
}